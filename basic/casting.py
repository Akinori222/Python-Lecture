# casting データ型の変換

# データ型のおさらい
# "1" : strings
# 1 : int
# 0.2 : float
# True, False : boolean
# [1, 2, 3] : list
# (1, 2, 3) : tuple
# {1: "one", 2: "two", 3: "three"} : dictionary
# {1, 2, 3} : set

# str(), int(), float(), bool(), list(), tuple(), set()

print(1 + int("1"))
print("1" + str(1))
print(float(1))

print(list("hello"))
print(bool(1))
print(bool(0))

print(tuple([1, 2, 3]))
print(set([1, 1, 2, 3]))  # リストから重複をなくす




